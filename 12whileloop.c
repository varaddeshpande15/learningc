// Loops can execute a block of code as long as a specified condition is reached.
// It save time, reduce errors, and they make code more readable.
// While Loop :
// The while loop loops through a block of code as long as a specified condition is true:
// while (condition) {
  // code block to be executed
// }
// The code in the loop will run, over and over again, as long as a variable (i) is less than 5:
// #include<stdio.h>
// int main()
// {
//     int i = 0;
// while (i < 5) {
//   printf("%d\n", i);
//   i++;
// }
// }
// Note: Do not forget to increase the variable used in the condition (i++), otherwise the loop will never end!
// The Do/While Loop : The do/while loop is a variant of the while loop.
// This loop will execute the code block once, before checking if the condition is true,
// then it will repeat the loop as long as the condition is true.
// do {
  // code block to be executed
//   }
// while (condition);
// {
//     int i = 0;
// do {
//   printf("%d\n", i);
//   i++;
// }
// while (i < 5);
// }
// Do not forget to increase the variable used in the condition, otherwise the loop will never end!